#include "minishell.h"

/**
 *
 * @param str A number is valid if contains digits. <br /> The first element is a sign or a digit.
 * @return int as a boolean
 */
int	ft_isnumber(const char *str)
{
	if (str[0] == '-' || str[0] == '+')
		str++;
	while (*str)
	{
		if (!ft_isdigit(*str))
			return (0);
		str++;
	}
	return (1);
}