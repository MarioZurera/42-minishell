#include "minishell.h"

int	ft_atoi(const char *nptr)
{
	int		num;
	int		sign;
	int		i;

	i = 0;
	while (ft_isspace(nptr[i]))
		++i;
	sign = 1;
	if (nptr[i] == '+' || nptr[i] == '-')
		if (nptr[i++] == '-')
			sign = -1;
	num = 0;
	while (ft_isdigit(nptr[i]))
	{
		num *= 10;
		num += nptr[i] - '0';
		++i;
	}
	return (num * sign);
}
